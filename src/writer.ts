import { writeFile, mkdir } from 'node:fs/promises';
import { join } from 'node:path';
import { toPascalCase } from './converter.js';
import type { AcfFieldGroup } from './types.js';

function fragmentName(group: AcfFieldGroup): string {
  return toPascalCase(group.graphql_field_name) + 'Fields';
}

function fragmentType(group: AcfFieldGroup): string {
  const type = group.graphql_types?.[0];
  if (!type) {
    console.warn(
      `  warning: ${group.title} has no graphql_types — defaulting to "Node". Set graphql_types in ACF or override in config.`
    );

    return 'Node';
  }

  return type;
}

function renderGraphQLFragment(group: AcfFieldGroup, fragment: string): string {
  const name = fragmentName(group);
  const type = fragmentType(group);

  return `# ${group.title} (auto-generated by acf-json-to-graphql)\nfragment ${name} on ${type} {\n${fragment}\n}\n`;
}

const green = (s: string): string => `\x1b[32m${s}\x1b[0m`;
const dim = (s: string): string => `\x1b[2m${s}\x1b[0m`;

export async function writeFragments(
  groups: AcfFieldGroup[],
  fragments: Map<string, string>,
  outDir: string
): Promise<void> {
  await mkdir(outDir, { recursive: true });

  let count = 0;
  for (const group of groups) {
    const fragment = fragments.get(group.key);
    if (!fragment) continue;
    const content = renderGraphQLFragment(group, fragment);
    const outPath = join(outDir, `${group.graphql_field_name}.graphql`);
    await writeFile(outPath, content, 'utf-8');
    console.log(`  ${green('✓')} ${dim(outPath)}`);
    count++;
  }

  console.log(`\n  ${green(`${count} fragment${count === 1 ? '' : 's'} generated`)}`);
}

export function printFragments(
  groups: AcfFieldGroup[],
  fragments: Map<string, string>
): void {
  for (const group of groups) {
    const fragment = fragments.get(group.key);
    if (!fragment) continue;
    console.log(renderGraphQLFragment(group, fragment));
  }
}
